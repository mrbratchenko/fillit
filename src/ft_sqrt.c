#include "fillit.h"

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
		return (0);
	while ((i * i) < nb)
	{
		i++;
	}
	if (i * i == nb && nb % i == 0)
		return (i);
	else
		return (0);
}
